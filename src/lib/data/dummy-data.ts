const createVariants = (basePrice: number) => {
  const sizes = ['S', 'M', 'L', 'XL'];
  const colors = ['Black', 'White', 'Navy', 'Olive'];
  
  return sizes.flatMap((size, sIndex) =>
    colors.map((color, cIndex) => ({
      id: `var-${sIndex}-${cIndex}`,
      title: `${size} / ${color}`,
      price: basePrice,
      available: Math.random() > 0.2,
      sku: `SKU-${size}-${color.toUpperCase().slice(0, 3)}`,
      options: [
        { name: 'Size', value: size },
        { name: 'Color', value: color },
      ],
    }))
  );
};

const createProduct = (
  id: number,
  title: string,
  description: string,
  price: number,
  images: string[],
  collections: string[],
  tags: string[],
  compareAtPrice?: number
): Product => ({
  id: `prod-${id}`,
  slug: title.toLowerCase().replace(/\s+/g, '-'),
  title,
  description,
  price,
  compareAtPrice,
  images: images.map((url, i) => ({
    id: `img-${id}-${i}`,
    url,
    altText: `${title} - Image ${i + 1}`,
    width: 800,
    height: 1000,
  })),
  variants: createVariants(price),
  collections,
  tags,
  available: true,
  createdAt: new Date(Date.now() - Math.random() * 30 * 24 * 60 * 60 * 1000).toISOString(),
});

